    private void btnAddClientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddClientActionPerformed
        // TODO add your handling code here:
        try{
            String clientName = txtClientName.getText();
            String clientAddress = txtClientAddress.getText();
            String clientPhone = txtClientPhone.getText();
            String clientEmail = txtClientEmail.getText();
            String clientAccount = txtClientAccount.getText();
            double clientBalance = Double.parseDouble(txtClientBalance.getText());

            if(clientName.equals("")|| clientAddress.equals("") || clientPhone.equals("") || clientEmail.equals("") || clientAccount.equals("") /*|| clientBalance. == ""*/) {
                JOptionPane.showMessageDialog(this, "Ingrese los valores requeridos en todos los campos!");
            }else {
                Account account = new Account(clientBalance, clientAccount);
                Client client = new Client(clientName, clientAddress, clientPhone, clientEmail, account);

                clientList.add(client);
                addClientsList();
                JOptionPane.showMessageDialog(rootPane, "El cliente ha sido añadido con éxito");
                cleanClientInputData();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ingrese un valor adecuado para el saldo");
        }
        
    }//GEN-LAST:event_btnAddClientActionPerformed


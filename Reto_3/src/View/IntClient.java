    public void addClientsList() {
        DefaultListModel model = new DefaultListModel();
        int indexModel = 0;
        for (Client client : clientList) {
            String clientInfo = client.getName() + "-" + client.getAddress() + "-" + client.getPhone() + "-" + client.getEmail() + "-" + client.getAccount().getAccountNumber() + "- $" + client.getAccount().getBalance();
            model.add(indexModel, clientInfo);
            indexModel++;
        }
        listClientsGui.setModel(model);
    }
    
    public void cleanClientInputData() {
        txtClientName.setText("");
        txtClientAddress.setText("");
        txtClientPhone.setText("");
        txtClientEmail.setText("");
        txtClientAccount.setText("");
        txtClientBalance.setText("");
    }
    
